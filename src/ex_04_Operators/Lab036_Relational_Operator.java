package ex_04_Operators;

public class Lab036_Relational_Operator {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a > b; // Is 10 > 30
        System.out.println(c);

        int age_Sachin = 33;
        int age_Rohit = 34;
        boolean result = age_Rohit >= age_Sachin;
        System.out.println(result);


    }
}
